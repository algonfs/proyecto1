                pagination : {
                    'total' : 0,
                    'current_page' : 0,
                    'per_page' : 0,
                    'last_page' : 0,
                    'from' : 0,
                    'to' : 0,
                },
                offset : 3,
                criterio : 'num_comprobante',
                buscar : '',
                criterio : 'nombre',
                criterioA: 'nombre',
                buscarA:'',
                arrayRuta: [],
                idruta: 0,
                nombre: '',
                precio: 0,
              
            }
        },
        components: {
            vSelect
        },
        computed:{
            isActived: function(){
                return this.pagination.current_page;
            },

            //Calcula los elementos de la paginación
            pagesNumber: function() {
                if(!this.pagination.to) {
                    return [];
                }
                
                var from = this.pagination.current_page - this.offset; 
                if(from < 1) {
                    from = 1;
                }

                var to = from + (this.offset * 2); 
                if(to >= this.pagination.last_page){
                    to = this.pagination.last_page;
                }  

                var pagesArray = [];
                while(from <= to) {
                    pagesArray.push(from);
                    from++;
                }
                return pagesArray;             

            },

            calcularTotal: function(){
                var resultado=0.0;
                for(var i=0;i<this.arrayDetalle.length;i++){
                    resultado=resultado+(this.arrayDetalle[i].precio*this.arrayDetalle[i].cantidad-this.arrayDetalle[i].descuento)
                }
                return resultado;
            }
        },
        methods : {
            listarVenta (page,buscar,criterio){
                let me=this;
                var url= '/venta?page=' + page + '&buscar='+ buscar + '&criterio='+ criterio;
                axios.get(url).then(function (response) {
                    var respuesta= response.data;
                    me.arrayVenta = respuesta.ventas.data;
                    me.pagination= respuesta.pagination;
                })
                .catch(function (error) {
                    console.log(error);
                });
            },
            selectCliente(search,loading){
                let me=this;
                loading(true)
                var url= '/cliente/selectCliente?filtro='+search;
                axios.get(url).then(function (response) {
                //console.log(response);
                    let respuesta= response.data;
                    q: search
                    me.arrayCliente = respuesta.clientes;
                    loading(false)
                })
                .catch(function (error) {
                    console.log(error);
                });
            },
            getDatosCliente(val1){
                let me = this;
                me.loading = true;
                me.idcliente = val1.id;
            },

            buscarRuta(){
                let me=this;
                var url= '/ruta/buscarRutaVenta?filtro=' + me.codigo;

                axios.get(url).then(function (response) {
                    var respuesta= response.data;
                    me.arrayRuta = respuesta.rutas;

                    if(me.arrayRuta.length>0){
                        me.nombre=me.arrayArticulo[0]['destino'];
                        me.idruta=me.arrayArticulo[0]['id'];
                        me.precio=me.arrayArticulo[0]['precio'];
                    }
                    else{
                        me.ruta='No existe esta Ruta';
                        me.idruta=0;
                    }
                })
                .catch(function (error){
                    console.log(error);
                });
            },
            pdfVenta(id){
                window.open('/venta/pdf/'+ id ,'_blank');
            },
            cambiarPagina(page,buscar,criterio){
                let me = this;
                //Actualiza la página actual
                me.pagination.current_page = page;
                //Envia la petición para visualizar la data de esa página
                me.listarVenta(page,buscar,criterio);
            },
            encuentra(id){
                var sw=0;
                for(var i=0;i<this.arrayDetalle.length;i++){
                    if(this.arrayDetalle[i].idruta==id){
                        sw=true;
                    }
                }
                return sw;
            },
            eliminarDetalle(index){
                let me = this;
                me.arrayDetalle.splice(index, 1);
            },
            agregarDetalle(){
                let me=this;
                if(me.idruta==0 || me.precio==0){
                }
                else{
                    if(me.encuentra(me.idruta)){
                        swal({
                            type: 'error',
                            title: 'Error...',
                            text: 'Este Destino ya se encuentra agregado!',
                        })
                    }
                   else{
                            me.arrayDetalle.push({
                            idruta: me.idruta,
                            destino: me.destino,
                            precio: me.precio,
                            
                            });
                            me.idruta=0;
                            me.destino='';
                            me.precio=0;
                        }
                    
                   
                }
                
            },
            agregarDetalleModal(data =[]){
                let me=this;
           /*     if(me.encuentra(data['id'])){
                        swal({
                            type: 'error',
                            title: 'Error...',
                            text: 'Este destino ya se encuentra agregado!',
                        })
                    }*/
                    me.arrayDetalle.push({
                    idruta: data['id'],
                    nombre: data['destino'],
                    precio: data['precio'],
                 
                    });
                    
            },
            listarRuta (buscar,criterio){
                let me=this;
                var url= '/ruta/listarRutaVenta?buscar='+ buscar + '&criterio='+ criterio;
                axios.get(url).then(function (response) {
                    var respuesta= response.data;
                    me.arrayRuta= respuesta.rutas.data;
                })
                .catch(function (error) {
                    console.log(error);
                });
            },
            registrarVenta(){
                if (this.validarVenta()){
                    return;
                }
                
                let me = this;

                axios.post('/venta/registrar',{
                    'idcliente': this.idcliente,
                    'tipo_comprobante': this.tipo_comprobante,
                    'serie_comprobante' : this.serie_comprobante,
                    'num_comprobante' : this.num_comprobante,
                    'total' : this.total,
                    'data' : this.arrayDetalle

                }).then(function (response) {
                    me.listado=1;
                    me.listarVenta(1,'','num_comprobante');
                    me.idproveedor=0;
                    me.tipo_comprobante='BOLETA';
                    me.serie_comprobante='';
                    me.num_comprobante='';
                    me.total=0.0;
                    me.idruta=0;
                    me.nombre='';
                    me.precio=0;
                    me.arrayDetalle=[];
                    window.open('/venta/pdf/'+ response.data.id);


                }).catch(function (error) {
                    console.log(error);
                });
            },
            validarVenta(){
                let me=this;
                me.errorVenta=0;
                me.errorMostrarMsjVenta =[];
              //  var art;

        /*        me.arrayDetalle.map(function(x){
                    if(x.cantidad>x.stock){
                        art=x.articulo +" Stock insuficiente";
                        me.errorMostrarMsjVenta.push(art);
                    }
                });*/

                if (me.idcliente==0) me.errorMostrarMsjVenta.push("Seleccione un Cliente");
                if (me.tipo_comprobante==0) me.errorMostrarMsjVenta.push("Sleccione el Comprobante");
                if (!me.num_comprobante) me.errorMostrarMsjVenta.push("Ingrese el numero de comprobante");
                if (me.arrayDetalle.length<=0) me.errorMostrarMsjVenta.push("Ingrese detalles");

                if (me.errorMostrarMsjVenta.length) me.errorVenta = 1;

                return me.errorVenta;
            },
            mostrarDetalle(){
                let me=this;
                me.listado=0;

                me.idcliente=0;
                    me.tipo_comprobante='BOLETA';
                    me.serie_comprobante='';
                    me.num_comprobante='';
                    me.total=0.0;
                    me.idruta=0;
